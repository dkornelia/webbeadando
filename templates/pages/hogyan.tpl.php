Hogyan válhatsz rendszeres adományozóvá? <br/>

Ha havonta, banki átutalásos megbízással szeretnéd átutalni adományod, kérjük, ezt az adomany@wwf.hu címen, telefonszámod és postai címed megadásával jelezd, hogy elküldhessük a megfelelő banki formanyomtatványt.<br/><br/>

Mi történik az adataiddal?<br/>

A bankkártyaadatok megadása és a bankkártyás fizetés a PayU bankkártya kezelő rendszeren történik. A PayU a kártyaadatokat a WWF felé nem továbbítja. A WWF az elektronikus űrlapon megadott adatokat az adományozás tényének rögzítése céljából tárolja. Biztosítunk, hogy alapítványunk az adataid kezelésekor a hatályos törvényi szabályozás szerint jár el.<br/><br/>

Mit takar a banki átutalásos megbízás?<br/>

Ez az egyik lehetősége annak, hogy havonta, rendszeres módon segítsd a WWF munkáját.<br/><br/>

<b>Két formában indítható el:<br/><br/></b>

1.) Első lépés, hogy az adományozó találkozik közterületen, rendezvényen vagy bevásárló központban munkatársunkkal, akinek segítségével kitölti az adomanyozási formanyomtatványt. Fontos, hogy a banknál bejelentett módon kell aláírni a dokumentumot. A nyomtatvány kitöltése után, az első példány az adomanyozónál marad. Második és a harmadik példány a WWF Magyarország irodájába kerül. Pár napon belül kollégánk felkeresi az adományozót a megadott telefonszámon adategyeztetés céljából, majd a WWF továbbítja a formanyomtatványt a banknak. Az első levonás körülbelül 4-8 hét után történik meg.
Fontos, hogy közterületen, rendezvényeken vagy bevásárló központokban dolgozó munkatársaink készpénzt nem fogadhatnak el.<br/>

2.) Az eljárás első lépése, hogy az adományozó az adomany@wwf.hu e-mail címre elküldje a postai elérhetőségét vagy telefonszámát. Ha ez megtörtént, akkor postai úton vagy személyesen eljuttatjuk a csoportos beszedési formanyomtatványt az adományozó részére. A nyomtatványt az adományozónak kell kitöltenie. Fontos, hogy a banknál bejelentett módon kell aláírni a dokumentumot. A nyomtatvány kitöltése után, a második és harmadik példányt az adományozónak vissza kell küldenie a WWF Magyarország Alapítvány részére, a 1141 Budapest Álmos Vezér útja 69/A. postacímre. Ezután a WWF továbbítja a formanyomtatványt a banknak. Az első levonás körülbelül 4-8 hét után történik meg.<br/><br/>

